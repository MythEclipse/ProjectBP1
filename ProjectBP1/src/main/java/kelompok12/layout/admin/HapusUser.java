    public void loadData(){
          kelompok12.database.repo.UserRepository userRepository = new kelompok12.database.repo.UserRepository();
        List<kelompok12.database.model.UserModel> users = userRepository.readAll();
        String[] columnNames = { "ID", "Username", "Password", "Jenis Kelamin", "Alamat", "Uang" };
        Object[][] data = new Object[users.size()][6];

        for (int i = 0; i < users.size(); i++) {
            kelompok12.database.model.UserModel user = users.get(i);
            data[i][0] = user.getId();
            data[i][1] = user.getUsername();
            data[i][2] = user.getPassword();
            data[i][3] = user.getJenisKelamin();
            data[i][4] = user.getAlamat();
            data[i][5] = user.getUang();
        }

        TableUser.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
    }


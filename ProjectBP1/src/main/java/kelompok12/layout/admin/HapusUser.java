    private void HapusButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HapusButtonMouseClicked
        String usernameToDelete = HapusTextField.getText();
        if (usernameToDelete.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kolom username tidak boleh kosong.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        int response = javax.swing.JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus pengguna: " + usernameToDelete + "?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION, javax.swing.JOptionPane.QUESTION_MESSAGE);
        if (response == javax.swing.JOptionPane.YES_OPTION) {
            kelompok12.database.repo.UserRepository userRepository = new kelompok12.database.repo.UserRepository();
            userRepository.deleteByUsername(usernameToDelete);
            javax.swing.JOptionPane.showMessageDialog(this, "Pengguna berhasil dihapus.", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadData();
        }
    }//GEN-LAST:event_HapusButtonMouseClicked

